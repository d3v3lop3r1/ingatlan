Pécsen a 7625-ös irányító szám térségében, frekventált helyen, közel a belvároshoz, egy felújított 8. emeleti panellakás eladó. 


<h4>Térelosztások</h4>

    2 szoba, 1 konyha beépített konyhabútorokkal, folyosó a bejárati ajtótól, a melyből a helyiségek nyílnak. 
    1 fürdő fürdőkáddal, 1 különálló helyiségben a WC, kb. 8 m2-es erkély, 5 m2-es tároló az alaksorban    

<h4>Lakás felszereltsége, adottságai</h4>



<h4>Ingatlan állaga</h4>

Modern műa. nyílászárók, duplaüveges szigetelt ablakok
Talapzat stabil járólapokkal és a szobákban laminált padlózat

<h4>Épület múltja</h4>

A 10 szintes magas épület építve 1970-1975 között, 
Felújítás utoljára 2011-ben
Masszív, száraz panel-épület, hőszigetelés nélkül
Lépcsőház felújításra került az elmúlt 15 évben
A villamosvezetékek már a legújabb standardnak megfelelően
A fűtőradiátorok nem lettek lecserélve

<h4>Távolságok az ingatlantól</h4>

Buszmegálló 500m, Élelmiszerbolt 400 m, Általános iskola 1.200 m
Autópálya 6 km, Budapest 220 km

<h4>Kiadások</h4>

Lakóközösségi közös-költség 6.000 Ft
Fűtési átalány 25.000 Ft/hó

<h4>Beköltözhető</h4>

30-45 napon belül

<h4>Amit büszkén említ tulajdonosa</h4>

Déli-keleti fekvése révén a nappali az erkélyen át örömteli napsütötte
Jó szomszédság, barátságos, emberi légkör a házban
Átlagnál kevesebb fűtési költség mivel a lakás védve van a szomszédlakásoktól 
A generál-kivitelezésű felújítás után a lakásban nincs semmi javítani való
Modern fürdőszoba új elemekkel

<h4>Adatgaranciák</h4>

Mivel a felsorolt adatok nagy része a megbízó tulajdonostól származnak, a hirdető azok pontosságáért nem vállal felelősséget. Az árajánlat a tulajdonossal való egyeztetéssel lett kitűzve, annak változtatási jogát, mint az eladás hogyanját a hirdető ingatlanfox fenntartja.

